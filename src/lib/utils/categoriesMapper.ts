export const IDToCategory = (id: number) => {
  switch (id) {
    case 0:
      return 'Creative Projects'
    case 1:
      return 'Technology and Innovation'
    case 2:
      return 'Food and Agriculture'
    case 3:
      return 'Social Causes'
    case 4:
      return 'Event Funding'
    case 5:
      return 'REFI'
    case 6:
      return 'Renewable Energy'
    case 7:
      return 'Animal Welfare'
  }
} 